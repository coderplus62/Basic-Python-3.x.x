# 1. Literal Constants
# Literal constants are fixed values used in Python code.
# Examples: 42, 3.14, "Hello, World!"

# 2. Numbers (Int and float)
# Integers and floating-point numbers are numeric data types.
# Examples:
integer_number = 42
float_number = 3.14

# 3. Strings
# Strings are sequences of characters enclosed in single or double quotes.
# Examples:
name = "Alice"
message = 'Hello, ' + name

# 4. Comments
# Comments are notes in code that aren't executed by the interpreter.
# They provide explanations to other developers or to yourself.
# Example:
# This is a comment explaining the purpose of the following line of code.

# 5. print() function
# The print() function outputs text to the console.
# Example:
print("Hello, World!")

# 6. format() function
# The format() function is used for string formatting.
# It replaces placeholders with provided values.
# Example:
age = 30
formatted_message = "I am {} years old.".format(age)

# 7. Escape sequences
# Escape sequences are special characters that start with a backslash (\).
# They allow including characters that are difficult to type directly.
# Example:
escaped_text = "This is a newline: \\n"

# 8. Indentation
# Indentation is used to structure code blocks in Python.
# It is crucial for defining the scope of statements in functions, loops, etc.
# Example:
if age < 18:
    print("You are a minor.")
else:
    print("You are an adult.")
