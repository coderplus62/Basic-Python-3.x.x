# 1. Variable Declaration and Assignment
# Variables are used to store data in memory for later use.
# They are created through assignment and don't need to be explicitly declared.
# Example:
name = "Alice"  # Here, 'name' is a variable storing the value "Alice"

# 2. Variable Naming Conventions
# Variables can have names with letters, digits, and underscores.
# They must start with a letter (or underscore) and are case-sensitive.
# Conventions:
# - Use descriptive names for clarity (e.g., 'name' instead of 'n')
# - Use lowercase letters for variable names (e.g., 'age' instead of 'Age')
# - Separate words with underscores (e.g., 'full_name' instead of 'fullname')
# - Avoid using reserved keywords as variable names (e.g., 'print', 'if', 'while')

# Valid variable names:
user_name = "Bob"
age = 25
_important_variable = True

